    INTO TABLE food
    CHARACTER SET utf8mb4
    FIELDS TERMINATED BY ','
    ENCLOSED BY '"'
    LINES TERMINATED BY '\n'
    IGNORE 1 ROWS
    (
     food_code,
     food_type,
     food_name,
     category_id,
     reference_amount,
     @energy,
     @protein,
     @fat,
     @carbs,
     @sugar,
     @sodium,
     food_weight,
     url
        )
    SET
        energy  = IFNULL(NULLIF(@energy, ''), 0),
        protein = IFNULL(NULLIF(@protein, ''), 0),
        fat     = IFNULL(NULLIF(@fat, ''), 0),
        carbs   = IFNULL(NULLIF(@carbs, ''), 0),
        sugar   = IFNULL(NULLIF(@sugar, ''), 0),
